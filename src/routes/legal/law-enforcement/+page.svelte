<section>
	<h1>Law Enforcement Guidelines</h1>
	<p>Last updated: October 20, 2025</p>

	<h2>About our data</h2>
	<p>
		We maintain a minimal amount of data. Security telemetry may be processed by our network edge
		and WAF. Feature data (e.g., messages sent to kidney bot) may be stored in our databases as
		described in our Privacy Policy.
	</p>

	<h2>Legal process required</h2>
	<p>
		We require a valid and properly scoped legal process (such as a subpoena, court order, or
		warrant) to disclose non‑public information. Requests must be issued under applicable law and
		directed to the correct entity.
	</p>

	<h2>Emergency requests</h2>
	<p>
		If there is an imminent risk of serious harm or death, law enforcement may submit an emergency
		disclosure request. Such requests must explain the nature of the emergency, the specific data
		requested, and why normal process cannot be obtained in time.
	</p>

	<h2>Notice to users</h2>
	<p>
		Our policy is to notify users of requests for their information unless legally prohibited or
		where we, in good faith, believe that providing notice would create a risk of harm or is
		otherwise inappropriate. We may delay notice when prohibited by law and provide notice after the
		restriction lifts.
	</p>

	<h2>Preservation requests</h2>
	<p>
		We will consider properly served preservation requests consistent with applicable law.
		Preservation does not create new data; we preserve existing records for a limited time pending
		legal process.
	</p>

	<h2>Jurisdiction</h2>
	<p>
		We are based in the United States. Non‑U.S. requests generally require a Mutual Legal Assistance
		Treaty (MLAT) process or other applicable international mechanism.
	</p>

	<h2>Contact</h2>
	<p>
		Law enforcement should contact: alec@alecj.com. Include official contact details, scope, and
		relevant legal citations. We may request verification.
	</p>

	<p>
		These guidelines are informational and not a commitment to produce any particular records. Refer
		to our Privacy Policy for additional details on data categories and retention.
	</p>
</section>

